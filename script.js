// Enhanced counter animation for stats
function animateCounters(entries) {
    const counters = document.querySelectorAll('.stat-number[data-target]');
    
    counters.forEach(counter => {
        const target = parseInt(counter.getAttribute('data-target'));
        const duration = 2500;
        const increment = target / (duration / 16);
        let current = 0;
        
        const timer = setInterval(() => {
            current += increment;
            if (current >= target) {
                current = target;
                clearInterval(timer);
            }
            counter.textContent = Math.floor(current);
        }, 16);
    });
}

// Trigger counter animation when results section is visible
const resultsSection = document.querySelector('.results-overview');
let countersAnimated = false;

const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting && !countersAnimated) {
            setTimeout(() => animateCounters(entries), 500);
            countersAnimated = true;
        }
    });
}, { threshold: 0.5 });

if (resultsSection) {
    counterObserver.observe(resultsSection);
}

